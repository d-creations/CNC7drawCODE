export class LetterDrawer {
    /**
     * @param {import("./DrawBoard.js").DrawBoard} drawBoard 
     */
    constructor(drawBoard) {
        this.drawBoard = drawBoard;
    }

    /**
     * Draws text based on stickFont lines on the draw board.
     * 
     * @param {string} text - The input text to be drawn.
     * @param {number} startX - Starting X coordinate for the first character.
     * @param {number} startY - Starting Y coordinate for the first character.
     * @param {number} size - Multiplication factor for the character size.
     * @param {"horizontal" | "vertical"} orientation - Text drawing direction.
     * @param {"high" | "low" | "normal"} mode - "high" maps everything to uppercase, "low" maps everything to lowercase.
     */
    drawLetters(text, startX, startY, size, orientation = "horizontal", mode = "normal") {
        let currentX = startX;
        let currentY = startY;

        // The spacing between consecutive letters
        const letterSpacing = size * 1.5;

        for (let i = 0; i < text.length; i++) {
            let charOrig = text[i];
            let charToDraw = charOrig;
            
            // Map "high" and "low" cases
            if (mode === "high") {
                charToDraw = charOrig.toUpperCase();
            } else if (mode === "low") {
                charToDraw = charOrig.toLowerCase();
            }

            if (charToDraw === ' ') {
                if (orientation === "horizontal") {
                    currentX += letterSpacing;
                } else {
                    currentY += letterSpacing;
                }
                continue;
            }

            const strokes = stickFont[charToDraw];

            if (strokes) {
                // Draw all line segments for the current character
                for (let stroke of strokes) {
                    const [p1, p2] = stroke;

                    // Calculate screen/board coordinates based on size and current cursor
                    const x1 = currentX + p1[0] * size;
                    const y1 = currentY + p1[1] * size;
                    const x2 = currentX + p2[0] * size;
                    const y2 = currentY + p2[1] * size;

                    const startObj = { exist: false, x: x1, y: y1 };
                    const endObj   = { exist: false, x: x2, y: y2 };

                    this.drawBoard.drawLine(startObj, endObj);
                }
            } else {
                console.warn(`Character '${charToDraw}' not found in stickFont.`);
            }

            // Move the cursor to the next character spot
            if (orientation === "horizontal") {
                currentX += letterSpacing;
            } else {
                currentY += letterSpacing;
            }
        }
    }
}
